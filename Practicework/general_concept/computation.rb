
# Halle Berry Farm Animal
farm_name = "Halle Berry Farm "
puts "Good day Sir/ Madam Welcome to " + farm_name + "And how may we help you ?"

puts "I want an animal"

puts "Please enter your animal name "

animal_name = gets.chomp

if(animal_name.casecmp"Ram")
  puts "Good for making suya"
elsif(animal_name.eql?"Goat".downcase!)
  puts "Good for pepper soup"
elsif(animal_name.eql?"Cat".downcase)
  puts "Good in drinking tea with a large cup"
elsif(animal_name.eql?"Sheep".downcase)
  puts "Good in making wool"
end

